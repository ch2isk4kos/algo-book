function rotateLeft(a, d) {
  const start = 1;
  const end = a + 1;
  const arr = Array.from({ length: end - start }, (x, i) => i + start)

  for (let i = 0; i < d; i++) {
    arr.push(arr.shift());
  }
  return arr;
}

rotateLeft(10, 5);  // [ 6, 7, 8, 9, 10, 1, 2, 3, 4, 5 ]